#include <stdio.h>
#include <stdlib.h>

/**
 * print_alphabet - function
 *
 * Description: 'contains the code for alphabets'
 *
 * Return: Always 0 (Success)
 */
void print_alphabet(void)
{
char c;
for (c = 'a'; c <= 'z'; c++)
{
putchar(c);
}

}
/**
 * main - Entry point
 *
 * Description: 'contains the alphabet function'
 *
 * Return: Always 0 (Success)
 */
void print_alphabet(void)
int main(void)
{
putchar(print_alphabet());

return (0);
}
